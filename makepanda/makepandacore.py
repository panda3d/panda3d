    manifest = ReadFile(fn)
    subst = " name='Microsoft.VC90.CRT' version='"+ver+"' "
    manifest = VC90CRTVERSIONRE.sub(subst, manifest)
    manifest = VC80CRTASSEMBLYRE.sub("", manifest)
    WriteFile(fn, manifest)

